-- STORED PROCEDURE
-- 1. Nhập vào mã lớp, in ra danh sách sinh viên thuộc lớp đó 
CREATE PROC DS_SV (@MALOP VARCHAR(30))
AS 
BEGIN
	SELECT S.MSSV, S.HOTEN, S.NGSINH, S.GIOITINH, S.NOISINH, S.MAKHOA FROM SINHVIEN AS S, LOP AS L, HOC AS H 
	WHERE S.MSSV = H.MSSV AND L.MALOP = H.MALOP AND L.MALOP = @MALOP 
END
-- 1.1. THỰC THI 
DECLARE @MALOP VARCHAR(30)
SET @MALOP = 'IE103.N21' -- CHỮ HOA --> THÀNH CÔNG 
EXEC DS_SV @MALOP 

DECLARE @MALOP VARCHAR(30)
SET @MALOP = 'it001.n21' -- CHỮ THƯỜNG --> THÀNH CÔNG 
EXEC DS_SV @MALOP 
-- 1.2 XÓA
DROP PROC DS_SV 

-- 2. Sinh viên nhập tên môn học sau đó hiển thị ra danh sách thông tin các lớp có dạy môn học đó để sinh viên 
-- chọn lớp phù hợp theo thời khóa biểu của mình khi đăng ký học phần 
CREATE PROC MH_LOP (@TENMH VARCHAR(40))
AS 
BEGIN
	SELECT L.MALOP, L.SISO, L.THU, L.TIET FROM LOP AS L, MONHOC AS M 
	WHERE L.MAMH = M.MAMH AND M.TENMH = @TENMH 
END
-- 2.1. THỰC THI 
DECLARE @TENMH VARCHAR(40)
SET @TENMH = 'quan ly thong tin' -- CHỮ THƯỜNG--> THÀNH CÔNG 
EXEC MH_LOP @TENMH  

DECLARE @TENMH VARCHAR(40)
SET @TENMH = 'QUAN LY THONG TIN' -- CHỮ HOA --> THÀNH CÔNG 
EXEC MH_LOP @TENMH  

DECLARE @TENMH VARCHAR(40)
SET @TENMH = 'qUaN Ly tHoNg TiN' -- CHỮ VỪA HOA VỪA THƯỜNG --> THÀNH CÔNG 
EXEC MH_LOP @TENMH  

-- 2.2 XÓA
DROP PROC MH_LOP

-- TRIGGER
-- 1. Sinh viên có DTB >= 5 thì đạt và ngược lại 
CREATE TRIGGER CHECK_KQ
ON KETQUA
FOR INSERT, UPDATE
AS
BEGIN
	IF ((SELECT DIEMTB FROM inserted) < 5 AND (SELECT KQUA FROM inserted) = 'Dau')
	BEGIN
			RAISERROR(N'Sinh vien co DTB < 5 thi ket qua phai la Khong dau', 16, 1)
            ROLLBACK TRANSACTION
    END 
	ELSE IF ((SELECT DIEMTB FROM inserted) >= 5 AND (SELECT KQUA FROM inserted) = 'Khong dau')
		BEGIN
			RAISERROR(N'Sinh vien co DTB >= 5 thi ket qua phai la Dau', 16, 1)
            ROLLBACK TRANSACTION
        END
END

