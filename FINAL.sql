CREATE VIEW THONGKE_DIEM
AS
	SELECT MH.MAMH, TENMH, SV.MSSV, HOTEN, DIEM
	FROM SINHVIEN SV JOIN KETQUATHI KQT ON SV.MSSV = KQT.MSSV JOIN MONHOC MH ON KQT.MAMH = MH.MAMH
